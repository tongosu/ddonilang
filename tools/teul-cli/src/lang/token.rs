use crate::lang::span::Span;

#[derive(Clone, Debug, PartialEq)]
pub enum TokenKind {
    String(String),
    Template(String),
    Assertion(String),
    FormulaBlock(String),
    Number(i64),
    Atom(String),
    Pragma(String),
    True,
    False,
    None,
    Salim,
    Boyeojugi,
    Tolabogi,
    Chaewugi,
    Susic,
    Start,
    EveryMadi,
    Halttae,
    Mada,
    Manyak,
    Iramyeon,
    Ilttae,
    Aniramyeon,
    Anigo,
    Majeumyeon,
    Jeonjehae,
    Bojanghago,
    Goreugi,
    Repeat,
    During,
    Beat,
    Reserve,
    Daehae,
    Break,
    ContinueLoop,
    Ident(String),
    Josa(String),
    Plus,
    PlusEqual,
    PlusArrow,
    Minus,
    MinusEqual,
    MinusArrow,
    Star,
    Slash,
    Percent,
    At,
    And,
    Or,
    RelationEq,
    EqEq,
    NotEq,
    Lt,
    Lte,
    Gt,
    Gte,
    Equal,
    Comma,
    LParen,
    RParen,
    LBracket,
    RBracket,
    LBrace,
    RBrace,
    Caret,
    Dot,
    DotDot,
    DotDotEq,
    Colon,
    Arrow,
    SignalArrow,
    Pipe,
    Newline,
    Eof,
}

#[derive(Clone, Debug)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}

impl Token {
    pub fn new(kind: TokenKind, span: Span) -> Self {
        Self { kind, span }
    }
}
