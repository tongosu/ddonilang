def build_age4_proof_source_snapshot_fields(
    *,
    top_snapshot: dict[str, object] | None = None,
    gate_result_snapshot: dict[str, object] | None = None,
    gate_result_present: bool | int | str = False,
    final_status_parse_snapshot: dict[str, object] | None = None,
    final_status_parse_present: bool | int | str = False,
) -> dict[str, str]:
    normalized_top = (
        build_age4_proof_snapshot(**top_snapshot)
        if isinstance(top_snapshot, dict)
        else build_age4_proof_snapshot()
    )
    normalized_gate = (
        build_age4_proof_snapshot(**gate_result_snapshot)
        if isinstance(gate_result_snapshot, dict)
        else build_age4_proof_snapshot()
    )
    normalized_final = (
        build_age4_proof_snapshot(**final_status_parse_snapshot)
        if isinstance(final_status_parse_snapshot, dict)
        else build_age4_proof_snapshot()
    )
    top_text = build_age4_proof_snapshot_text(normalized_top)
    gate_text = build_age4_proof_snapshot_text(normalized_gate)
    final_text = build_age4_proof_snapshot_text(normalized_final)
    gate_present_flag = bool(gate_result_present)
    final_present_flag = bool(final_status_parse_present)
    return {
        AGE4_PROOF_GATE_RESULT_SNAPSHOT_TEXT_KEY: gate_text,
        AGE4_PROOF_GATE_RESULT_SNAPSHOT_PRESENT_KEY: "1" if gate_present_flag else "0",
        AGE4_PROOF_GATE_RESULT_SNAPSHOT_PARITY_KEY: (
            "1" if gate_present_flag and gate_text == top_text else "0"
        ),
        AGE4_PROOF_FINAL_STATUS_PARSE_SNAPSHOT_TEXT_KEY: final_text,
        AGE4_PROOF_FINAL_STATUS_PARSE_SNAPSHOT_PRESENT_KEY: "1" if final_present_flag else "0",
        AGE4_PROOF_FINAL_STATUS_PARSE_SNAPSHOT_PARITY_KEY: (
            "1" if final_present_flag and final_text == top_text else "0"
        ),
    }


